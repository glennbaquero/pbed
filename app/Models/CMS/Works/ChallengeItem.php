<?php

namespace App\Models\CMS\Works;

use App\Extenders\Models\BaseModel as Model;

use App\Models\CMS\Works\WorkChallenge;

class ChallengeItem extends Model
{
	protected $fillable	= [
		'work_challenge_id', 'name', 'description',
		'graphs'
	];

	protected $casts = ['graphs' => 'array'];
	
	/*
	|--------------------------------------------------------------------------
	| @Consts
	|--------------------------------------------------------------------------
	*/



	/*
	|--------------------------------------------------------------------------
	| @Attributes
	|--------------------------------------------------------------------------
	*/



	/*
	|--------------------------------------------------------------------------
	| @Relationships
	|--------------------------------------------------------------------------
	*/


	public function challenge()
	{
		return $this->belongsTo(WorkChallenge::class);
	}


	/*
	|--------------------------------------------------------------------------
	| @Methods
	|--------------------------------------------------------------------------
	*/


	public function toSearchableArray()
    {
        return [
			'id' => $this->id,
        ];
    }


	/*
	|--------------------------------------------------------------------------
	| @Renders
	|--------------------------------------------------------------------------
	*/



	/*
	|--------------------------------------------------------------------------
	| @Checkers
	|--------------------------------------------------------------------------
	*/
}
